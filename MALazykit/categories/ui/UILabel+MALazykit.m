#import "UILabel+MALazykit.h"

@implementation UILabel (MALazykit)

+ (instancetype)label {
    return [[self alloc] init];
}

+ (instancetype)labelWithFrame:(CGRect)frame {
    return [[self alloc] initWithFrame:frame];
}

@end
